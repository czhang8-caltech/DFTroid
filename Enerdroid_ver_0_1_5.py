def run_check_termination_mode(current_dir: Path) -> None:
    out_files = sorted(current_dir.glob("*.out"))

    print_banner(current_dir, extra=f"检测到 / Detected {len(out_files)} 个 .out 文件")
    print_section("检测 .out 正常结束 / Check .out termination")

    if not out_files:
        print_status("FAIL", "当前文件夹没有 .out 文件 / No .out files found in current directory.")
        return

    normal_files = []
    abnormal_files = []

    start_time = time.time()
    total = len(out_files)

    for idx, out_file in enumerate(out_files, start=1):
        print_progress(idx - 1, total, start_time, prefix="检测进度 / Check")

        try:
            text = read_text(out_file)
            info = analyze_out_file(text)

            if info["job_status"] == "SUCCESS":
                normal_files.append(out_file.name)
            else:
                abnormal_files.append({
                    "file": out_file.name,
                    "job_status": info["job_status"],
                    "optimization_completed": info["optimization_completed"],
                    "error_codes": info["error_codes"],
                    "error_messages": info["error_messages"],
                })

        except Exception as e:
            abnormal_files.append({
                "file": out_file.name,
                "job_status": "UNFINISHED",
                "optimization_completed": False,
                "error_codes": [],
                "error_messages": [],
                "read_error": str(e),
            })

        print_progress(idx, total, start_time, prefix="检测进度 / Check")

    failed_count = sum(1 for x in abnormal_files if x.get("job_status") == "FAILED")
    unfinished_count = sum(1 for x in abnormal_files if x.get("job_status") == "UNFINISHED")

