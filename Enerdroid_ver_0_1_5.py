def collect_statistics_pairs(current_dir: Path) -> tuple[list[dict], list[str]]:
    opt_files = sorted(current_dir.glob("*.out"))
    records = []
    skipped_messages = []

    total = len(opt_files)
    start_time = time.time()

    for idx, opt_file in enumerate(opt_files, start=1):
        print_progress(idx - 1, total, start_time, prefix="统计扫描 / Stats")

        stem = opt_file.stem
        if stem.endswith("_SP"):
            print_progress(idx, total, start_time, prefix="统计扫描 / Stats")
            continue

        expected_sp_stem = make_output_stem(stem, "_SP")
        sp_file = current_dir / f"{expected_sp_stem}.out"

        if not sp_file.exists():
            skipped_messages.append(f"[跳过] {opt_file.name} 缺少配对单点文件 {sp_file.name}")
            print_progress(idx, total, start_time, prefix="统计扫描 / Stats")
            continue

        try:
            opt_text = read_text(opt_file)
            sp_text = read_text(sp_file)

            opt_status = get_gaussian_job_status(opt_text)
            sp_status = get_gaussian_job_status(sp_text)

            if opt_status != "SUCCESS":
                skipped_messages.append(f"[跳过] {opt_file.name} 未成功完成（status={opt_status}）")
                print_progress(idx, total, start_time, prefix="统计扫描 / Stats")
                continue

            if sp_status != "SUCCESS":
                skipped_messages.append(f"[跳过] {sp_file.name} 未成功完成（status={sp_status}）")
                print_progress(idx, total, start_time, prefix="统计扫描 / Stats")
                continue

            opt_charge, opt_mult = extract_charge_and_multiplicity(opt_text)
            sp_charge, sp_mult = extract_charge_and_multiplicity(sp_text)

            tce = extract_tce(opt_text)
            tcg = extract_tcg(opt_text)
            spe = extract_spe(sp_text)
            h_value = tce + spe
            g_value = tcg + spe

            charge_value: Any = opt_charge if opt_charge == sp_charge else f"mismatch: OPT={opt_charge}, SP={sp_charge}"
            mult_value: Any = opt_mult if opt_mult == sp_mult else f"mismatch: OPT={opt_mult}, SP={sp_mult}"

            records.append({
                "key": stem,
                "name": stem,
                "family_key": family_key_from_name(stem),
                "family_anchor": name_similarity_anchor(stem),
                "structure": "",
                "OPT_File": str(opt_file.resolve()),
                "Thermal correction to Enthalpy (TCE)": tce,
                "Thermal correction to Gibbs Free Energy (TCG)": tcg,
                "single point energy (SPE)": spe,
                "enthalpies (H)": h_value,
                "Gibbs free energies (G)": g_value,
                "SP__File": str(sp_file.resolve()),
                "charge": charge_value,
                "multiplicity": mult_value,
            })

        except Exception as e:
            skipped_messages.append(f"[跳过] {opt_file.name} / {sp_file.name} 统计失败: {e}")

        print_progress(idx, total, start_time, prefix="统计扫描 / Stats")

    return records, skipped_messages


