def read_existing_rows(ws, canonical_to_col: dict[str, int]) -> tuple[list[dict], dict[str, dict]]:
    all_rows = []
    keyed_rows_map = {}

    for row in range(2, ws.max_row + 1):
        row_cells = [ws.cell(row=row, column=col) for col in range(1, ws.max_column + 1)]
        values = [cell.value for cell in row_cells]

        styles = []
        for cell in row_cells:
            styles.append({
                "font": copy.copy(cell.font),
                "fill": copy.copy(cell.fill),
                "border": copy.copy(cell.border),
                "alignment": copy.copy(cell.alignment),
                "number_format": cell.number_format,
                "protection": copy.copy(cell.protection),
                "hyperlink": copy.copy(cell.hyperlink),
            })

        canonical_values = {}
        for canonical, col in canonical_to_col.items():
            canonical_values[canonical] = ws.cell(row=row, column=col).value

        row_data = {
            "row_index": row,
            "values": values,
            "styles": styles,
            "canonical_values": canonical_values,
        }
        all_rows.append(row_data)

        key = detect_row_key(canonical_values)
        if key:
            keyed_rows_map[key] = row_data

    return all_rows, keyed_rows_map


def compare_core_data(old_canonical_values: dict[str, Any], new_record: dict) -> list[str]:
    changes = []
    fields = [
        "name",
        "OPT_File",
        "Thermal correction to Enthalpy (TCE)",
        "Thermal correction to Gibbs Free Energy (TCG)",
        "single point energy (SPE)",
        "SP__File",
        "charge",
        "multiplicity",
    ]

    for field in fields:
        old_val = old_canonical_values.get(field)
        new_val = new_record.get(field)
        old_num = try_float(old_val)
        new_num = try_float(new_val)

        if old_num is not None and new_num is not None:
            if abs(old_num - new_num) > 1e-10:
                changes.append(f"{field}: {old_val} -> {new_val}")
        else:
            if str(old_val) != str(new_val):
                changes.append(f"{field}: {old_val} -> {new_val}")

    return changes


