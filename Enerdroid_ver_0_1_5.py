def read_text(file_path: Path) -> str:
    encodings = ["utf-8", "gb18030", "latin-1"]
    for enc in encodings:
        try:
            return file_path.read_text(encoding=enc, errors="replace")
        except Exception:
            continue
    raise RuntimeError(f"无法读取文件 / Cannot read file: {file_path}")


def normalize_header_name(text: str) -> str:
    return re.sub(r"\s+", " ", str(text).strip().lower())


def canonicalize_header(text: str) -> str | None:
    norm = normalize_header_name(text)
    for canonical, aliases in CORE_COLUMN_ALIASES.items():
        for alias in aliases:
            if norm == normalize_header_name(alias):
                return canonical
    return None


