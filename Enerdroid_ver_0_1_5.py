def get_gaussian_job_status(text: str) -> str:
    """
    Return:
    - SUCCESS
    - FAILED
    - UNFINISHED

    Rule:
    1. Find all termination-related events in the whole file.
    2. Use the LAST termination-related event as the final verdict.
    3. If the last one is 'Normal termination of Gaussian' -> SUCCESS
    4. If the last one is 'Error termination ...' -> FAILED
    5. If neither exists -> UNFINISHED
    """
    termination_events = []

    normal_pattern = re.compile(r"Normal termination of Gaussian", re.IGNORECASE)
    error_pattern = re.compile(r"Error termination", re.IGNORECASE)

    for m in normal_pattern.finditer(text):
        termination_events.append((m.start(), "SUCCESS"))

    for m in error_pattern.finditer(text):
        termination_events.append((m.start(), "FAILED"))

    if not termination_events:
        return "UNFINISHED"

    termination_events.sort(key=lambda x: x[0])
    return termination_events[-1][1]


def has_normal_termination(text: str) -> bool:
    return get_gaussian_job_status(text) == "SUCCESS"


def has_optimization_completed(text: str) -> bool:
    return "Optimization completed." in text


def extract_charge_and_multiplicity(text: str) -> tuple[int, int]:
    matches = re.findall(r"Charge\s*=\s*(-?\d+)\s+Multiplicity\s*=\s*(-?\d+)", text)
    if not matches:
        raise ValueError("未找到电荷和多重度信息 / Charge and multiplicity not found.")
    charge_str, mult_str = matches[-1]
    return int(charge_str), int(mult_str)


def extract_error_codes(text: str) -> list[str]:
    codes = []
    patterns = [
        r"Error termination via Lnk1e.*?[/\\](l\d+)\.exe",
        r"Error termination.*?[/\\](l\d+)\.exe",
        r"[/\\](l\d+)\.exe",
        r"\b(l\d+)\.exe\b",
    ]
    for pat in patterns:
        found = re.findall(pat, text, flags=re.IGNORECASE | re.DOTALL)
        for item in found:
            code = item.lower()
            if code not in codes:
                codes.append(code)
    return codes


def extract_common_error_messages(text: str) -> list[str]:
    found_messages = []
    for pattern, label in COMMON_ERROR_PATTERNS:
        if re.search(pattern, text, flags=re.IGNORECASE):
            if label not in found_messages:
                found_messages.append(label)
    return found_messages


def extract_last_float_after_label(text: str, label_pattern: str) -> float:
    matches = re.findall(label_pattern, text, flags=re.IGNORECASE)
    if not matches:
        raise ValueError(f"未找到目标字段 / Target field not found: {label_pattern}")
    return float(matches[-1])


def extract_tce(text: str) -> float:
    return extract_last_float_after_label(
        text,
        r"Thermal correction to Enthalpy=\s*([-+]?\d+\.\d+)"
    )


def extract_tcg(text: str) -> float:
    return extract_last_float_after_label(
        text,
        r"Thermal correction to Gibbs Free Energy=\s*([-+]?\d+\.\d+)"
    )


def extract_spe(text: str) -> float:
    matches = re.findall(
        r"SCF Done:\s+E\([^)]+\)\s*=\s*([-+]?\d+\.\d+)",
        text,
        flags=re.IGNORECASE
    )
    if not matches:
        raise ValueError("未找到单点能 / SCF Done energy not found.")
    return float(matches[-1])


def analyze_out_file(text: str) -> dict:
    status = get_gaussian_job_status(text)
    return {
        "job_status": status,  # SUCCESS / FAILED / UNFINISHED
        "normal_termination": status == "SUCCESS",
        "optimization_completed": has_optimization_completed(text),
        "error_codes": extract_error_codes(text),
        "error_messages": extract_common_error_messages(text),
    }


def family_key_from_name(name: str) -> str:
    if "_" in name:
        parts = name.split("_")
        if len(parts) >= 2:
            return "_".join(parts[:-1]).strip().lower()
    return name.strip().lower()


def name_similarity_anchor(name: str) -> str:
    return re.sub(r"(_v\d+)$", "", name.strip().lower())


