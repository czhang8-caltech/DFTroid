def find_latest_energy_excel(current_dir: Path) -> Path | None:
    folder_name = current_dir.name
    pattern = re.compile(rf"^{re.escape(folder_name)}_Energy_(\d{{6}})\.xlsx$", re.IGNORECASE)

    matched = []
    for f in current_dir.glob("*.xlsx"):
        m = pattern.match(f.name)
        if m:
            matched.append((m.group(1), f))

    if not matched:
        return None

    matched.sort(key=lambda x: x[0], reverse=True)
    return matched[0][1]


