def get_route_line_interactively(current_dir: Path) -> str | None:
    config = load_config(current_dir)
    last_route = str(config.get("last_route_line", "")).strip()

    if last_route:
        print_section("转换设置 / Convert settings")
        print("检测到上一次的 route line / Last saved route line detected:")
        print(f"  {last_route}\n")
        answer = input("直接回车 = 使用上一次；输入 n = 输入新的\n> ").strip().lower()

        if answer == "":
            return last_route

        if answer == "n":
            new_route = input("\n请输入新的单点能 route line / Please enter a new route line:\n> ").strip()
            if not new_route:
                print_status("FAIL", "route line 不能为空 / route line cannot be empty.")
                return None
            config["last_route_line"] = new_route
            save_config(current_dir, config)
            return new_route

        print_status("FAIL", "输入无效 / Invalid input.")
        return None

    new_route = input("\n请输入单点能 route line / Please enter the single-point route line:\n> ").strip()
    if not new_route:
        print_status("FAIL", "route line 不能为空 / route line cannot be empty.")
        return None

    config["last_route_line"] = new_route
    save_config(current_dir, config)
    return new_route


def run_convert_mode(current_dir: Path) -> None:
    out_files = sorted(current_dir.glob("*.out"))

    print_banner(current_dir, extra=f"检测到 / Detected {len(out_files)} 个 .out 文件")
    print_section("转换模式 / Convert mode")

    if not out_files:
        print_status("FAIL", "当前文件夹没有 .out 文件 / No .out files found in current directory.")
        return

    route_line = get_route_line_interactively(current_dir)
    if not route_line:
        return

    suffix = input("\n请输入输出后缀（回车默认 _SP）/ Output suffix (Enter for _SP):\n> ").strip()
    if not suffix:
        suffix = "_SP"

    success_count = 0
    failed_count = 0
    skipped_count = 0

    start_time = time.time()
    total = len(out_files)

    for idx, out_file in enumerate(out_files, start=1):
        print_progress(idx - 1, total, start_time, prefix="转换进度 / Convert")

        try:
            output_stem = make_output_stem(out_file.stem, suffix)
            output_file = out_file.with_name(output_stem + ".gjf")

            if output_file.exists():
                print(f"\n[跳过 / SKIP] {out_file.name} -> {output_file.name} 已存在 / already exists.")
                skipped_count += 1
                continue

            text = read_text(out_file)

            if not has_optimization_completed(text):
                raise ValueError("未检测到 Optimization completed.")

            job_status = get_gaussian_job_status(text)
            if job_status != "SUCCESS":
                codes = extract_error_codes(text)
                messages = extract_common_error_messages(text)
                extra = [f"status={job_status}"]
                if codes:
                    extra.append("codes=" + ", ".join(codes))
                if messages:
                    extra.append("messages=" + "; ".join(messages))
                extra_text = f" ({' | '.join(extra)})" if extra else ""
                raise ValueError("Gaussian 未正常结束 / not normally terminated" + extra_text)

            charge, multiplicity = extract_charge_and_multiplicity(text)
            coords = extract_last_orientation_block(text)

            write_gjf(
                output_path=output_file,
                route_line=route_line,
                title=output_stem,
                charge=charge,
                multiplicity=multiplicity,
                coords=coords,
            )

            print(f"\n[成功 / OK] {out_file.name} -> {output_file.name} (charge={charge}, multiplicity={multiplicity})")
            success_count += 1

        except Exception as e:
            print(f"\n[失败 / FAIL] {out_file.name}: {e}")
            failed_count += 1

        print_progress(idx, total, start_time, prefix="转换进度 / Convert")

    print_summary_block([
        "转换完成 / Conversion finished",
        f"成功 / Success : {success_count}",
        f"跳过 / Skipped : {skipped_count}",
        f"失败 / Failed  : {failed_count}",
    ])


