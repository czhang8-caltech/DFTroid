def handle_exit(current_dir: Path) -> None:
    config = load_config(current_dir)
    custom_exit = str(config.get("custom_exit_message", "")).strip()

    print_section("退出设置 / Exit settings")
    if custom_exit:
        print(f"当前自定义结束语 / Current custom exit line:\n  {custom_exit}")
    else:
        print("当前自定义结束语 / Current custom exit line:\n  (无 / none)")

    new_line = input(
        "\n输入新的结束语 = 覆盖；直接回车 = 保持；输入 clear = 清空\n> "
    ).strip()

    if new_line.lower() == "clear":
        config["custom_exit_message"] = ""
        save_config(current_dir, config)
        custom_exit = ""
        print_status("OK", "自定义结束语已清空 / Custom exit line cleared.")
    elif new_line:
        config["custom_exit_message"] = new_line
        save_config(current_dir, config)
        custom_exit = new_line
        print_status("OK", "自定义结束语已更新 / Custom exit line updated.")

    print("\n" + FIXED_EXIT_SLOGAN)
    if custom_exit:
        print(custom_exit)


