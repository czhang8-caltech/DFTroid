def print_section(title_cn_en: str, char: str = "-") -> None:
    print("\n" + line(char))
    print(title_cn_en)
    print(line(char))


def print_summary_block(lines_: list[str]) -> None:
    print("\n" + line("="))
    for item in lines_:
        print(item)
    print(line("="))


def print_status(tag: str, message: str) -> None:
    print(f"[{tag}] {message}")


def print_progress(current: int, total: int, start_time: float, prefix: str = "") -> None:
    if total <= 0:
        return
    ratio = current / total
    bar_len = 30
    filled = int(bar_len * ratio)
    bar = "█" * filled + "-" * (bar_len - filled)
    elapsed = time.time() - start_time
    eta = (elapsed / current) * (total - current) if current > 0 else 0
    print(
        f"\r{prefix} [{bar}] {current}/{total} "
        f"({ratio*100:5.1f}%) | elapsed {elapsed:6.1f}s | ETA {eta:6.1f}s",
        end="",
        flush=True,
    )
    if current == total:
        print()


