def build_header_maps(ws) -> tuple[dict[str, int], dict[int, str]]:
    canonical_to_col = {}
    col_to_canonical = {}

    for col in range(1, ws.max_column + 1):
        value = ws.cell(row=1, column=col).value
        if value is None:
            continue
        canonical = canonicalize_header(str(value))
        if canonical and canonical not in canonical_to_col:
            canonical_to_col[canonical] = col
            col_to_canonical[col] = canonical

    return canonical_to_col, col_to_canonical


def ensure_core_columns(ws) -> dict[str, int]:
    canonical_to_col, _ = build_header_maps(ws)

    if "name" not in canonical_to_col:
        ws.insert_cols(1, amount=1)
        ws.cell(row=1, column=1).value = "name"

    canonical_to_col, _ = build_header_maps(ws)

    if "structure" not in canonical_to_col:
        ws.insert_cols(2, amount=1)
        ws.cell(row=1, column=2).value = "structure"

    canonical_to_col, _ = build_header_maps(ws)

    for canonical in CANONICAL_HEADER_ORDER:
        if canonical not in canonical_to_col:
            col = ws.max_column + 1
            ws.cell(row=1, column=col).value = canonical

    canonical_to_col, _ = build_header_maps(ws)
    return canonical_to_col


