def print_main_menu(current_dir: Path) -> None:
    config = load_config(current_dir)
    custom_exit = str(config.get("custom_exit_message", "")).strip()
    last_route = str(config.get("last_route_line", "")).strip()

    extras = [
        f"检测到 / Detected {len(list(current_dir.glob('*.out')))} 个 .out 文件",
        f"检测到 / Detected {len(list(current_dir.glob('*.gjf')))} 个 .gjf 文件",
    ]
    if last_route:
        extras.append(f"上次 route line / Last route line: {last_route}")
    if custom_exit:
        extras.append(f"自定义结束语 / Custom exit line: {custom_exit}")

    print_banner(current_dir, extra="\n".join(extras))
    print_section("主菜单 / Main menu")
    print("  1 - 转换模式 / Convert .out to single-point .gjf")
    print("  2 - 检测模式 / Check mode")
    print("  3 - 统计模式 / Build or update Energy Excel")
    print("  4 - 退出 / Exit")


def main() -> None:
    current_dir = Path.cwd()

    while True:
        print_main_menu(current_dir)
        choice = input("\n请输入 1 / 2 / 3 / 4:\n> ").strip()

        if choice == "1":
            run_convert_mode(current_dir)
        elif choice == "2":
            run_check_mode(current_dir)
        elif choice == "3":
            run_statistics_mode(current_dir)
        elif choice == "4":
            handle_exit(current_dir)
            break
        else:
            print_status("FAIL", "输入无效 / Invalid selection.")


