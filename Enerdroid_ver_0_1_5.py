def extract_last_orientation_block(text: str):
    lines = text.splitlines()
    standard_starts = []
    input_starts = []

    for i, line_ in enumerate(lines):
        if "Standard orientation:" in line_:
            standard_starts.append(i)
        elif "Input orientation:" in line_:
            input_starts.append(i)

    if standard_starts:
        block_start = standard_starts[-1]
    elif input_starts:
        block_start = input_starts[-1]
    else:
        raise ValueError("未找到最终坐标块 / No final orientation block found.")

    dash_indices = []
    for i in range(block_start, min(block_start + 20, len(lines))):
        if re.match(r"\s*-{5,}\s*$", lines[i]):
            dash_indices.append(i)

    if len(dash_indices) < 2:
        raise ValueError("坐标表格式异常 / Orientation block format error.")

    data_start = dash_indices[1] + 1
    coords = []

    for i in range(data_start, len(lines)):
        line_ = lines[i]
        if re.match(r"\s*-{5,}\s*$", line_):
            break

        parts = line_.split()
        if len(parts) < 6:
            continue

        try:
            atomic_number = int(parts[1])
            x = float(parts[3])
            y = float(parts[4])
            z = float(parts[5])
        except ValueError:
            continue

        symbol = ELEMENTS.get(atomic_number)
        if symbol is None:
            raise ValueError(f"未知原子序数 / Unknown atomic number: {atomic_number}")

        coords.append((symbol, x, y, z))

    if not coords:
        raise ValueError("未解析到坐标 / No coordinates parsed.")

    return coords


def write_gjf(output_path: Path, route_line: str, title: str, charge: int, multiplicity: int, coords) -> None:
    chk_name = output_path.with_suffix(".chk").name
    with output_path.open("w", encoding="utf-8", newline="\n") as f:
        f.write(f"%chk={chk_name}\n")
        f.write(route_line.strip() + "\n\n")
        f.write(title + "\n\n")
        f.write(f"{charge} {multiplicity}\n")
        for symbol, x, y, z in coords:
            f.write(f"{symbol:<3s} {x:>16.8f} {y:>16.8f} {z:>16.8f}\n")
        f.write("\n")


